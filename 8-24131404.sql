DROP VIEW IF EXISTS `вирт_Направление_предварительно_Кол-во`;
CREATE VIEW `вирт_Направление_предварительно_Кол-во` AS
SELECT
  `фак`.`Код` AS `Код факультета`,
  `фак`.`Название` AS `Название факультета`,
 COUNT(*) AS `Число направлений`
FROM `Справка+` AS `напр`
LEFT JOIN `Справка+` AS `фак` ON `напр`.`Ид_родитель` = `фак`.`Ид`
WHERE `напр`.`Слк` = 'Напр'
GROUP BY `фак`.`Код`
;

SELECT * FROM `вирт_Направление_предварительно_Кол-во`;


